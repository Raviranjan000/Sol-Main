<script>
link_url('ug','1');
var cnt = 0;
	function link_url(type,url){
	
	if(type == 'ug'){	
		$('#myModalFirst').modal('show');	
	
		document.getElementById("float_div_first").innerHTML = '<iframe width="100%" height="400" src="admission_notice.html" frameborder="0" allowfullscreen></iframe>';	
		document.getElementById("type_button").innerHTML = '<button type="button" class="btn btn-warning" onclick="close_all()">Proceed</button>';
	}
	else if(cnt == '0'){
		document.getElementById("float_div_first").innerHTML = '<iframe width="100%" height="400" src="admission_notice.html" frameborder="0" allowfullscreen></iframe>';
		document.getElementById("type_button").innerHTML = '<button type="button" class="btn btn-warning" onclick="return link_url_next(\''+type+'\',\''+url+'\')">Proceed</button>';
		cnt = '1';
	}
	else{
		$("#myModalFirst").modal('hide');
		$("#first_box").html('');
		link_url_next(type,url);
		return false;
	}
	return false;
}

	function link_url_next(type,url){ 	
	$("#myModalFirst").modal('hide');
	$("#myModal").modal('show');
	
	if(type == 'pdf'){
		document.getElementById("float_div").innerHTML = '<iframe width="100%" height="400" src="'+url+'" frameborder="0" allowfullscreen></iframe>';
		document.getElementById("type_name").innerHTML = 'Guidelines';
	}
	else if(type == 'image'){
		document.getElementById("float_div").innerHTML = '<iframe width="100%" height="400" src="images/'+url+'" frameborder="0" allowfullscreen></iframe>';
		document.getElementById("type_name").innerHTML = 'Fee Structure';
	}	
	else if(type == 'english'){
		location.href='next.php';
	}
	else if(type == 'hindi'){
		location.href='next_hindi.php';
	}
	else if(type == 'link'){
		document.getElementById("float_div").innerHTML = '<h1 style="color:gray;">Coming Soon...</h1>';
		document.getElementById("type_name").innerHTML = 'Link';
	}	
	else{
		document.getElementById("float_div").innerHTML = '<iframe width="100%" height="400" src="'+url+'" frameborder="0" allowfullscreen></iframe>';
		document.getElementById("type_name").innerHTML = 'Video';
	}
	
	return false;
}

function close_all(){
	$("#float_div_first").html('');
	$('#myModalFirst').modal('hide');	
}

function close_all_next(){
	$("#float_div").html('');
}

